public enum VyprDatePickerFormat
{
    /// <summary>
    /// The date only
    /// </summary>
    DateOnly,

    /// <summary>
    /// The time only 12 h
    /// </summary>
    TimeOnly12H,

    /// <summary>
    /// The time only 24 h
    /// </summary>
    TimeOnly24H,

    /// <summary>
    /// The date and time 12 h
    /// </summary>
    DateAndTime12H,

    /// <summary>
    /// The date and time 24 h
    /// </summary>
    DateAndTime24H,
}